    "            conn.close()\n",
    "            if row is None:\n",
    "                return None\n",
    "            m_start = row[0]\n",
    "            L_prev = np.frombuffer(row[1], dtype=np.float64).copy()\n",
    "            L = np.frombuffer(row[2], dtype=np.float64).copy()\n",
    "            res = np.frombuffer(row[3], dtype=np.float64).copy()\n",
