    "            conn.close()\n",
    "            break\n",
    "        except sqlite3.OperationalError:\n",
    "            time.sleep(2)\n",
    "\n",
    "def load_log_Q_all_state(n, b, db_name='recursions.db'):\n",
    "    for _ in range(5):\n",
    "        try:\n",
    "            conn = sqlite3.connect(db_name, timeout=60.0)\n",
    "            c = conn.cursor()\n",
    "            c.execute('''SELECT m_start, L_prev, L, res, log_poch FROM log_Q_all_states WHERE n=? AND b=?''', (n, b))\n",
