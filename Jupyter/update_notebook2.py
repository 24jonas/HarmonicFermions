    "                      (n, b, m_start, L_prev.tobytes(), L.tobytes(), log_poch))\n",
    "            conn.commit()\n",
    "            conn.close()\n",
    "            break\n",
    "        except sqlite3.OperationalError:\n",
    "            time.sleep(2)\n",
    "\n",
    "def load_log_Q_state(n, b, db_name='recursions.db'):\n",
    "    for _ in range(5):\n",
    "        try:\n",
    "            conn = sqlite3.connect(db_name, timeout=60.0)\n",
    "            c = conn.cursor()\n",
    "            c.execute('''SELECT m_start, L_prev, L, log_poch FROM log_Q_states WHERE n=? AND b=?''', (n, b))\n",
    "            row = c.fetchone()\n",
    "            conn.close()\n",
    "            if row is None:\n",
    "                return None\n",
    "            m_start = row[0]\n",
    "            L_prev = np.frombuffer(row[1], dtype=np.float64).copy()\n",
    "            L = np.frombuffer(row[2], dtype=np.float64).copy()\n",
    "            log_poch = row[3]\n",
    "            return m_start, L_prev, L, log_poch\n",
    "        except sqlite3.OperationalError:\n",
    "            time.sleep(2)\n",
    "    return None\n",
    "\n",
    "def save_log_Q_all_state(n, b, m_start, L_prev, L, res, log_poch, db_name='recursions.db'):\n",
    "    for _ in range(5):\n",
    "        try:\n",
    "            conn = sqlite3.connect(db_name, timeout=60.0)\n",
    "            c = conn.cursor()\n",
    "            c.execute('''INSERT OR REPLACE INTO log_Q_all_states (n, b, m_start, L_prev, L, res, log_poch)\n",
    "                         VALUES (?, ?, ?, ?, ?, ?, ?)''', \n",
    "                      (n, b, m_start, L_prev.tobytes(), L.tobytes(), res.tobytes(), log_poch))\n",
